    public function notify()
    {
        //获取用户未读消息。
        $manager_id = \Auth::guard('admin')->user()->id;
        $unreadNews = News::where('receiver', $manager_id)
            ->where('is_read', 0)->orderBy('created_at', 'desc')->paginate(5);

        return response()->json($unreadNews);
    }
